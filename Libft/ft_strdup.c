#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*ptr;
	int		len;

	i = 0;
	len = ft_strlen(src);
	if (!(ptr = malloc(len * sizeof(*ptr) + 1)))
		return (NULL);
	while (src[i])
	{
		ptr[i] = src[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
