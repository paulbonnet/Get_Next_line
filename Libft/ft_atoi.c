int		ft_atoi(const char *str)
{
	int		i;
	int		j;
	int		nb;

	i = 0;
	j = 0;
	nb = 0;
	while (str[i] == ' ' || str[i] == '\v' || str[i] == '\n' ||
			str[i] == '\r' || str[i] == '\t' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		j = i;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = (nb * 10) + (str[i] - '0');
		i++;
	}
	if (str[j] == '-')
		nb = -nb;
	return (nb);
}
