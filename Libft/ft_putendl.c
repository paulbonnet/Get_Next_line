#include "libft.h"

void	ft_putendl(char const *s)
{
	int i;

	i = 0;
	if (s != 0)
	{
		while (s[i])
		{
			ft_putchar(s[i]);
			i++;
		}
	}
	ft_putchar('\n');
}
