#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*s;
	size_t	i;

	i = 0;
	if (!(s = malloc(size + 1)))
		return (NULL);
	while (i < size)
	{
		s[i] = 0;
		i++;
	}
	s[i] = '\0';
	return (s);
}
